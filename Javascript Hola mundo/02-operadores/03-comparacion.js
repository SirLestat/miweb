let a = 10;

//Operadores relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//Operadores de igualdad
console.log(a != 10);
console.log(a == 10);   //<----- '==' solo compara valores
console.log(a === '10');    //<----- '===' compara valores y tipos de datos
console.log(a !== '10');    //<----- '!==' compara si los valores y tipos de datos son distintos

