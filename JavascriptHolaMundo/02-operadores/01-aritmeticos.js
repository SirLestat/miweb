let a = 5;
let b = 7;

console.log(a + b, 'suma');   //<----- suma o adición 
console.log(a - b, 'resta');   //<----- resta
console.log(a * b, 'multiplicación');   //<----- multiplicación
console.log(a / b, 'división');   //<----- división
console.log(a % b, 'módulo');   //<----- módulo
console.log(a ** b, 'potencia');   //<----- potencia



//operador de incrementar

console.log(++a);   //<----- incrementa en 1 el vlaor de 'a'
console.log(a++);   //<----- imprime el valor de 'a' y despúes incrementa en 1
console.log(a);

//operador de decremento

console.log(--a);   //<----- decrementa en 1 el vlaor de 'a'
console.log(a--);   //<----- imprime el valor de 'a' y despúes decrementa en 1
console.log(a);